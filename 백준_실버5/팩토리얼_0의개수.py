'''
재귀함수를 이용하는 경우 --> 런타임 에러
while 문을 이용하는 경우 --> 시간 초과

즉, factorial 함수를 직접 구현하지 않고 값을 계산해야함.

idea : 2, 5의 곱의 개수 즉 10의 거듭제곱의 개수를 구해야 한다.
여기서 모든 곱은 2보다는 5의 개수를 찾는 것이 낫다 거의 2가 더 많기 때문에 5를 찾는 다고 생각하자.

5,4,3,2,1 => 5의 개서 1개

'''


n = int(input())

# 5의 개수를 찾습니다.
count_five = 0
i = 5
while (n/i >= 1):
    count_five += n//i # 10//5 ==> 5의 개수 2개
    i *= 5

print(count_five)
