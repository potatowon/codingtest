'''
두개의 정 N 면체와 정 M 면체의 두개의 주사위를 던져서 나올 수 있는 눈의 합중 가장 확률이 높은 숫자를 출력하는 프로그램

 [1, 2, 3, 4]
 [1, 2, 3, 4, 5 ,6]

[2, 3, 4, 5, 6, 7]
    [3, 4, 5, 6, 7, 8]
        [4, 5, 6, 7, 8, 9]
            [5, 6, 7, 8, 9, 10]
4, 6
5,6,7 3개

[2,3,4,5,6,7,8,9]
    [3,4,5,6,7,8,9,10]
        [4,5,6,7,8,9,10,11]
            [5,6,7,8,9,10,11,12]
4, 8
5,6,7,8,9 5개
'''

import sys

N, M = map(int, sys.stdin.readline().split())
m = max(M, N)
n = min(M, N)
result = [n+i for i in range(1, (m-n+1)+1)]
print(*result)
